void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	buffer_t* nfp;
	if (huart == &MD_UART)
		nfp = &toPcBf;
	else
		nfp = &toMdBf;

	nfp->datEnd += nfp->lastRxDif;
	if (nfp->datEnd >= BFR_SIZE)
		nfp->datEnd = 0;

	nfp->lastRxDif = EACH_RECV_LEN;
    HAL_UART_Receive_IT(huart, (nfp->bf)+(nfp->datEnd), EACH_RECV_LEN);
}

